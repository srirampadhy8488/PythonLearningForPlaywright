# Integer variable (number of test cases)
number_of_test_cases = 15
print("Number of test cases:", number_of_test_cases)

# Float variable (test coverage in percentage)
test_coverage = 89.5
print("Test coverage:", test_coverage)

# String variable (test case name)
test_case_name = "Login Functionality"
print("Test case name:", test_case_name)

# Boolean variable (did the test pass?)
test_passed = True
print("Test passed?", test_passed)